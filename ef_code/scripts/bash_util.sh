#!/bin/bash
#Functions
#---------------------------------------------------------------------------
pretty_echo() {
	echo -e "\033[1;32m ################################################## \n"
    echo -e "\033[1;32m [$(date -u '+%Y-%m-%d %H:%M:%S UTC')] $1 \n"
	echo -e "\033[1;32m ################################################## \e[0m"

}
error_echo() {
	echo -e "\033[1;31m ################################################## \n"
    echo -e "\033[1;31m [$(date -u '+%Y-%m-%d %H:%M:%S UTC')] $1 \n"
	echo -e "\033[1;31m ################################################## \e[0m"

}
warning_echo() {
	echo -e "\033[1;33m ################################################## \n"
    echo -e "\033[1;33m [$(date -u '+%Y-%m-%d %H:%M:%S UTC')] $1 \n"
	echo -e "\033[1;33m ################################################## \e[0m"

}
question_echo() {
    echo -e "\033[1;34m [$(date -u '+%Y-%m-%d %H:%M:%S UTC')] $1 \n"
}

file_echo() {
echo -e "\033[1;33;40m --------------------------------------------------------------------\n"

while read line;do
	echo -e "\033[1;33;40m $line "

done < $1
echo -e "\033[1;33;40m --------------------------------------------------------------------\e[0m"

}
pretty_csv() {
	cat "$@" |perl -pe 's/(?!"), /+/g;' |  perl -pe 's/((?<=,)|(?<=^)),/ ,/g;' | column -t -s, | less -S
}

